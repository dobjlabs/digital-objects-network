use std::{collections::BTreeSet, error::Error, fs, path::PathBuf};

use driver::{DriverActionCatalog, PexeCatalog};

fn render_union_type(name: &str, values: impl IntoIterator<Item = String>) -> String {
    let mut uniq = BTreeSet::new();
    for value in values {
        uniq.insert(value);
    }
    let body = if uniq.is_empty() {
        "never".to_string()
    } else {
        uniq.into_iter()
            .map(|value| format!("{value:?}"))
            .collect::<Vec<_>>()
            .join(" | ")
    };
    format!("export type {name} = {body};")
}

/// Pack every plugin under `plugins/` at the repo root into in-memory pexe bytes
/// and feed them to [`PexeCatalog`]. This keeps codegen tied to the in-repo
/// source of truth (no dependency on `~/.dobj/actions/` being populated).
fn load_repo_catalog() -> Result<PexeCatalog, Box<dyn Error>> {
    let manifest_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let plugins_dir = manifest_dir.join("../../plugins");
    let mut entries = Vec::new();
    for entry in fs::read_dir(&plugins_dir)? {
        let entry = entry?;
        let path = entry.path();
        if !path.is_dir() {
            continue;
        }
        let source = pexe::PluginSource::read(&path)?;
        let bytes = pexe::pack(&source.manifest_toml, &source.script)?;
        let name = source.parse_manifest()?.plugin.name.clone();
        entries.push((PathBuf::from(format!("{name}.pexe")), bytes));
    }
    Ok(PexeCatalog::from_bytes(entries, true)?)
}

fn render_typescript_ids() -> Result<String, Box<dyn Error>> {
    let catalog = load_repo_catalog()?;
    let action_union = render_union_type(
        "ActionId",
        catalog.list_actions().into_iter().map(|action| action.id),
    );
    let class_union = render_union_type(
        "ClassName",
        catalog
            .list_classes()
            .into_iter()
            .map(|class_info| class_info.name),
    );
    Ok(format!(
        r#"/* eslint-disable */
// This file is auto-generated by `cargo run --manifest-path app-gui/src-tauri/Cargo.toml --example gen_ids`.
// Do not edit by hand.

{action_union}
{class_union}
"#
    ))
}

fn main() -> Result<(), Box<dyn Error>> {
    let manifest_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let output_path = manifest_dir.join("../src/shared/generated/ids.ts");
    let ids = render_typescript_ids()?;
    if let Some(parent) = output_path.parent() {
        fs::create_dir_all(parent)?;
    }
    fs::write(&output_path, ids)?;
    println!("generated {}", output_path.display());
    Ok(())
}
